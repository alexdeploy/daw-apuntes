    private void jButtonValidarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonValidarActionPerformed
        
        String url = "jdbc:mysql://172.21.0.2:3306/myDb?serverTimezone=UTC";
        
        try {
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            
            Connection conn = (Connection) DriverManager.getConnection(url, "root", "");
            
            Statement st = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            
            ResultSet rs = st.executeQuery("SELECT * FROM personas");
            
            // Guardo en variables los textos infroducidos
            String usuarioIntroducido = jTextFieldUser.getText();
            String passwordIntroducido = String.valueOf(jPass.getPassword());
            
            String usuarioBd = rs.getString("usuario");
            String passwordBd = rs.getString("password");
            
            if (usuarioIntroducido.equals(usuarioBd) && passwordIntroducido.equals(passwordBd)) {
                jLabelValidar.setText("Usuario Existe");
            } else {
            
                jLabelValidar.setText("Usuario no existe");
            }
            
        } catch (Exception ex) {
            
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_jButtonValidarActionPerformed

